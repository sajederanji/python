shoae = float(input('give me shoae:'))
ertefa = float(input('give me ertefa:'))
pi = 3.14
masahat_janebi = (2*pi*shoae*ertefa)
masahat_kol = (masahat_janebi+2*pi*shoae*shoae)
hajm = (pi*shoae*shoae*ertefa)
print('masahat_janebi_ostovane:' ,masahat_janebi)
print('masahat_kol_ostovane:' ,masahat_kol)
print('hajm_ostovane:' ,hajm)